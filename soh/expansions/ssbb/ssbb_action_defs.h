// ── Action Metadata ─────────────────────────────────────────────────────────

#define SSBB_ACT_FLAG_LOOP       (1 << 0)  // Animation loops
#define SSBB_ACT_FLAG_ATTACK     (1 << 1)  // Has hitbox
#define SSBB_ACT_FLAG_AIRBORNE   (1 << 2)  // Expects airborne state
#define SSBB_ACT_FLAG_GROUNDED   (1 << 3)  // Expects grounded state
#define SSBB_ACT_FLAG_MOVEMENT   (1 << 4)  // Can move during action
#define SSBB_ACT_FLAG_INVULN     (1 << 5)  // Invulnerable during action (dodges)
#define SSBB_ACT_FLAG_LOCKED     (1 << 6)  // Cannot be interrupted by movement selection (items, specials)

typedef struct {
    SSBBActionId id;
    PikachuAnimId animId;       // Index into pikachu_ssbb_all_anims[]
    u8 flags;
    u8 hitboxStartFrame;        // 0 = no hitbox
    u8 hitboxEndFrame;
    u8 cancelFrame;             // Frame after which action can be interrupted (0 = end of anim)
} SSBBActionDef;

