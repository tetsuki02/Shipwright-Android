// ── Helper: get action def ──
static inline const SSBBActionDef* SSBBAction_Get(SSBBActionId id) {
    if (id >= SSBB_ACT_MAX) return NULL;
    return &sSSBBActionTable[id];
}

// ── Helper: get SSBBAnim for action ──
static inline const struct SSBBAnim* SSBBAction_GetAnim(SSBBActionId id) {
    const SSBBActionDef* def = SSBBAction_Get(id);
    if (!def || def->animId >= PIKA_ANIM_MAX) return NULL;
    return pikachu_ssbb_all_anims[def->animId];
}

