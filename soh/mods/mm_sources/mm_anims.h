// ============================================================================
// Animation Definition Struct
// ============================================================================

/**
 * MM Animation definition
 * Contains all info needed to load and use an MM animation
 */
typedef struct {
    const char* path; // OTR path (misc/link_animetion/..._Data)
    s16 frameCount;   // Number of frames (from MM decomp)
    u8 limbCount;     // Number of limbs (22 for Human Link)
} MmAnimDef;

// ============================================================================
// Limb Counts
// ============================================================================

// ALL MM Link forms use 22 limbs (confirmed from 2Ship: LINK_*_LIMB_MAX = 0x16)
// Visual differences come from different display lists per limb, not limb count.
// This allows all forms to share the same animation data format (67 s16 per frame).
#define MM_LIMB_COUNT_HUMAN 22 // Human Link, Child Link
#define MM_LIMB_COUNT_GORON 22 // Goron Link (LINK_GORON_LIMB_MAX = 0x16)
#define MM_LIMB_COUNT_ZORA 22  // Zora Link (LINK_ZORA_LIMB_MAX = 0x16)
#define MM_LIMB_COUNT_DEKU 22  // Deku Link (LINK_DEKU_LIMB_MAX = 0x16)

// ============================================================================
// Frame Data Constants
// ============================================================================

// Raw frame format: limbCount * 3 components + 1 appearanceInfo = 67 for Human
