/**
 * @file mm_anims.h
 * @brief MM Animation Definitions for use in OOT
 *
 * This file defines all MM animations with their OTR paths and frame counts.
 * Use with MmAnim_Load() from anim_translator/mm_anim_loader.h
 *
 * CRITICAL: OOT and MM use IDENTICAL raw format for Link animations!
 * - 67 s16 per frame (66 components + 1 appearanceInfo)
 * - Same layout (root pos, root rot, limb rotations)
 * - Only fix needed: baseTransl (X=-57, Z=0)
 * - Uses LinkAnimationHeader (NOT AnimationHeader!)
 *
 * Animation paths follow 2Ship pattern:
 * - Header: objects/gameplay_keep/gPlayerAnim_*
 * - Data: misc/link_animetion/gPlayerAnim_*_Data
 */

